class Product {
    constructor(id, name, model, serialNumber, description, quantityInStock, price, warrantyStatus, distributorInfo) {
      this.id = id;
      this.name = name;
      this.model = model;
      this.serialNumber = serialNumber;
      this.description = description;
      this.quantityInStock = quantityInStock;
      this.price = price;
      this.warrantyStatus = warrantyStatus;
      this.distributorInfo = distributorInfo;
    }
  }